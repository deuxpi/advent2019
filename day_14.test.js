const ReactionGraph = require('./day_14')

test('nanofactory', () => {
  const g = new ReactionGraph()
  g.addReaction('10 ORE => 10 A')
  g.addReaction('1 ORE => 1 B')
  g.addReaction('7 A, 1 B => 1 C')
  g.addReaction('7 A, 1 C => 1 D')
  g.addReaction('7 A, 1 D => 1 E')
  g.addReaction('7 A, 1 E => 1 FUEL')
  expect(g.orePerFuel()).toBe(31)
})

test('microfactory', () => {
  const g = new ReactionGraph()
  g.addReaction('9 ORE => 2 A')
  g.addReaction('8 ORE => 3 B')
  g.addReaction('7 ORE => 5 C')
  g.addReaction('3 A, 4 B => 1 AB')
  g.addReaction('5 B, 7 C => 1 BC')
  g.addReaction('4 C, 1 A => 1 CA')
  g.addReaction('2 AB, 3 BC, 4 CA => 1 FUEL')
  expect(g.orePerFuel()).toBe(165)
})

test('minifactory', () => {
  const g = new ReactionGraph()
  g.addReaction('157 ORE => 5 NZVS')
  g.addReaction('165 ORE => 6 DCFZ')
  g.addReaction('44 XJWVT, 5 KHKGT, 1 QDVJ, 29 NZVS, 9 GPVTF, 48 HKGWZ => 1 FUEL')
  g.addReaction('12 HKGWZ, 1 GPVTF, 8 PSHF => 9 QDVJ')
  g.addReaction('179 ORE => 7 PSHF')
  g.addReaction('177 ORE => 5 HKGWZ')
  g.addReaction('7 DCFZ, 7 PSHF => 2 XJWVT')
  g.addReaction('165 ORE => 2 GPVTF')
  g.addReaction('3 DCFZ, 7 NZVS, 5 HKGWZ, 10 PSHF => 8 KHKGT')
  expect(g.orePerFuel()).toBe(13312)
  expect(g.fuel(1000000000000)).toBe(82892753)
})

